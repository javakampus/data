package belajar.swing.jtabel;

public class Produk{
    private String simbol;
	private String nama;
	private int harga; 
	
	public void setSimbol(String simbol){
		this.simbol = simbol;
	}
	
	public String getSimbol(){
		return this.simbol;
	}
	
	public void setNama(String nama){
		this.nama = nama;
	}
	
	public String getNama(){
		return this.nama;
	}
	
	public void setHarga(int harga){
		this.harga = harga;
	}
	
	public int getHarga(){
		return this.harga;
	}
	
}
